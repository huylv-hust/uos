			<h3 id="a10" class="tit_sub">Q10. 残業はありますか？ </h3>
			<div class="box container">
				<p>
					はい、あります。時間外労働の場合、25%割増計算にて手当てが支払われます。<br />
					<br />
					例えば、時給1000円の方が残業した場合、次の計算になります。（1日の労働時間の内訳）<br />
					<br />
					10時間拘束、1時間休憩の場合（8時～18時のシフト　9時間労働）<br />
					基本給（働いた時間に得る金額）9,000円<br />
					残業手当（残業に当たる時間の割り増し分：1時間分）250円<br />
					その日の総額は9,250円になります。<br />
					<br />
					残業手当は、給与明細上は基本給と分けて記載されます。計算式は理解しておきましょう。
				</p>
