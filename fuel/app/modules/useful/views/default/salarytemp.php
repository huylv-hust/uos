			<h3 id="a1" class="tit_sub">Q1.  ガソリンスタンドの派遣スタッフの平均月収はどの程度ですか？</h3>
			<div class="box container">
				<p>
					就業形態（日勤や夜勤など）や、労働時間などにより差が生じますので、10万円～25万円の間の方が多くおられます。<br />
					短時間派遣の仕事を選択される方もいらっしゃいますので、平均月収は3万円～30万円弱と幅があります。個別にご相談頂ければ、ある程度は明確にお答えできますので、お気軽にご相談ください。<br />
					無料コンシェルジュへのご相談は<a href="/concierge" title="">こちら</a>から
				</p>
			</div>

			<h3 id="a2" class="tit_sub">Q2.  ガソリンスタンドの派遣スタッフでもボーナスなどありますか？</h3>
			<div class="box container">
				<p>
					基本的にボーナスはありません。派遣スタッフという働き方のほとんどが時給での給与計算ですので、働いていただいた時間分の給与のみとなります。<br />
					1年以上派遣スタッフとして在籍されている方を前提に、奨励金形式にて支給される場合があります。
				</p>
			</div>

			<h3 id="a3" class="tit_sub">Q3.  危険物乙四の資格をもっているのですが、手当ては付きますか？</h3>
			<div class="box container">
				<p>
					危険物乙四保持に対する資格手当ては基本的にありません。（求人情報によります）<br />
					募集情報の中で、資格をお持ちの方の時給がお持ちでない方の時給よりも高い場合、資格手当分の時給差とお考えください。
				</p>
				<p>
					資格をお持ちの方が条件等優遇されるのは事実です。<br />
					危険物乙四の資格保持者の場合、時給が高い就業先も選択できますし、その他にもセルフサービス型の店舗で一人就労（ワンオペレーションとも言います）する働き方も選べます。<br />
					給油許可の操作は有資格者に限定されますので、資格のない方は一人での就業形態を選ぶことはできません。こちらの就業形態は昼間の同じ就労時間よりも効率よく稼げ、身体的にも楽な仕事になります。
				</p>
			</div>

			<h3 id="a4" class="tit_sub">Q4.  社会保険に加入できますか？</h3>
			<div class="box container">
				<p>
					はい、加入できます。加入条件は以下の通りです。<br />
					<span class="calc"> 【社会保険加入条件の目安】月17日以上、130時間以上、且つ3ヶ月以上の契約</span><br />
					<spa class="notice">※2016年10月より、週20時間以上、且つ月80時間以上働く方で、昼間の学生以外は加入となります。</span>
				</p>
			</div>

			<h3 id="a5" class="tit_sub">Q5.  深夜勤務だけを行った場合の平均月収はどの程度になりますか？</h3>
			<div class="box container">
				<p>
					平均的な月収は20万円～24万円程度（社会保険加入者）です。<br />
					深夜短時間シフト（4時間又は6時間勤務）の方の平均的な月収は7万円～8万円です。
				</p>
			</div>

			<h3 id="a6" class="tit_sub">Q6.  正社員になることはできますか？</h3>
			<div class="box container">
				<p>
					はい、正社員になることも可能です。<br />
					まずエリア社員制度（採用地から通勤可能な範囲内での転勤辞令のみ）で、地域限定の正社員採用となります。<br />
					その後、ご本人の希望等を考慮し、勤務地制約のない正社員にもなることができます。
				</p>
			</div>

			<h3 id="a7" class="tit_sub">Q7.  派遣・業務請負で働く際のメリット・デメリットを教えてください。</h3>
			<div class="box container">
				<p>
					派遣形態で働くメリットは、同じ仕事に就労する場合、時給が高いので高収入になりますデメリットとしては、短期間の就労契約が多いこと、契約期間満了の際に更新されない事（失職の可能性）などがあります。<br />
				</p>
				<p>
					業務請負形態で働くメリットは、年齢制限がない（高齢の方でも働ける）情報が多い点や、仕事を覚えやすい（セルフサービス型の給油許可のボタン押し業務の場合等）点です。<br />
					デメリットは、夜間の請負形態で働くスタッフの場合だと生活サイクルが変わってしまう点などがあります。<br />
					いずれの働き方もメリット・デメリットがありますので、ご自身のライフスタイルに合った働き方を選ぶようにしてください。
				</p>
			</div>

			<h3 id="a8" class="tit_sub">Q8.  派遣・業務請負の場合の給料は時給計算ですか？</h3>
